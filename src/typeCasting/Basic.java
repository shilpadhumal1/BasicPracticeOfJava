package typeCasting;

public class Basic
{
	//Type casting is nothing but converting one type of entity in to another type
	//two types of type casting is in java
	//up casting
	//down casting
	public static void main(String[] args) 
	{
		int a=100;
		long b=(long)a;
		System.out.println(b);// upcasting because here byte is getting convert to higher order i.e integer
	}

}
